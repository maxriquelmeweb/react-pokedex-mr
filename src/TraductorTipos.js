export const traducirTipo = (tipo) => {
    if (tipo === 'fire') {
        return 'Fuego';
    }
    if (tipo === 'electric') {
        return 'Eléctrico';
    }
    if (tipo === 'normal') {
        return 'Normal';
    }
    if (tipo === 'grass') {
        return 'Planta';
    }
    if (tipo === 'poison') {
        return 'Veneno';
    }
    if (tipo === 'flying') {
        return 'Volador';
    }
    if (tipo === 'water') {
        return 'Agua';
    }
    if (tipo === 'bug') {
        return 'Bicho';
    }
    if (tipo === 'ground') {
        return 'Tierra';
    }
    if (tipo === 'fairy') {
        return 'Hada';
    }
    if (tipo === 'fighting') {
        return 'Lucha';
    }
    if (tipo === 'psychic') {
        return 'Psíquico';
    }
    if (tipo === 'rock') {
        return 'Roca';
    }
    if (tipo === 'steel') {
        return 'Acero';
    }
    if (tipo === 'ice') {
        return 'Hielo';
    }
    if (tipo === 'ghost') {
        return 'Fantasma';
    }
    if (tipo === 'dragon') {
        return 'Dragon';
    }
    if (tipo === 'dark') {
        return 'Siniestro';
    }
    return tipo;
}